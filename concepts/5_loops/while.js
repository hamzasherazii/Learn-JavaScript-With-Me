// This is a simple example of a while loop in JavaScript

// Initialize a counter variable
let counter = 0;

// The while loop will run as long as the condition (counter < 5) is true
while (counter < 5) {
    // Print the current value of counter
    console.log(counter);
    
    // Increment the counter by 1
    counter++;
}

// Explanation:
// 1. The counter variable is initialized to 0.
// 2. The while loop checks if the counter is less than 5.
// 3. If the condition is true, the code inside the loop is executed.
// 4. The current value of counter is printed to the console.
// 5. The counter is incremented by 1.
// 6. The loop repeats until the counter is no longer less than 5.