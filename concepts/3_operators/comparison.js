// Comparison Operators in JavaScript

// Equal to (==)
console.log(5 == '5'); // true, because it performs type coercion

// Not equal to (!=)
console.log(5 != '5'); // false, because it performs type coercion

// Strict equal to (===)
console.log(5 === '5'); // false, because it does not perform type coercion

// Strict not equal to (!==)
console.log(5 !== '5'); // true, because it does not perform type coercion

// Greater than (>)
console.log(5 > 3); // true

// Greater than or equal to (>=)
console.log(5 >= 5); // true

// Less than (<)
console.log(3 < 5); // true

// Less than or equal to (<=)
console.log(3 <= 5); // true